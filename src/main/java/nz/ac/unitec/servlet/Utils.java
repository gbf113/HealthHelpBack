package nz.ac.unitec.servlet;

public class Utils {

	public Utils() {

	}

	public int plus(int a, int b) {

		return a + b;
	}

	public int minus(int a, int b) {

		return a - b;
	}

	public int multy(int a, int b) {

		return a * b;
	}

	public float trad(int a, int b) {

		return (float) (a * 1.000 / b);
	}
}
